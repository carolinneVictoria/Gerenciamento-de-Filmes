    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
            // TODO add your handling code here:
            Avaliacao minhaAvaliacao = new Avaliacao();
            Usuario meuUsuario = new Usuario();

            minhaAvaliacao.setNota(Double.parseDouble(txtNota.getText()));
            minhaAvaliacao.setComentario(txtComentario.getText());

           
            minhaAvaliacao.setUsuario((Usuario) usuarioComboBox.getSelectedItem());
            minhaAvaliacao.setFilme((Filme) filmeComboBox.getSelectedItem());

            //instanciar um objeto da classe DAO
            AvaliacaoDAO dao = new AvaliacaoDAO();

            //invocar o metodo create da classe
            dao.create(minhaAvaliacao);
        
            limpar();
            preencherTabela();                      
    }//GEN-LAST:event_btnSalvarActionPerformed

