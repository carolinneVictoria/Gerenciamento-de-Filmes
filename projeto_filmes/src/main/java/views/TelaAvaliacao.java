    public void preencherTabela(){
            DefaultTableModel dtm = (DefaultTableModel) tblAvaliacao.getModel();
            dtm.setRowCount(0);
            AvaliacaoDAO dao = new AvaliacaoDAO();
            
            for(Avaliacao a : dao.read()){
                dtm.addRow(new Object[] {
                    a.getId_avaliacao(),
                    a.getUsuario().getNome(),
                    a.getFilme().getTitulo(),
                    a.getNota(),
                    a.getComentario(),
                    a.getData_criacao()
                });
            }
    }
    
    public void preencherUsuarios(){
        UsuarioDAO dao = new UsuarioDAO();
        for(Usuario u : dao.read()){
            usuarioComboBox.addItem(u);
        }
    }
    public void preencherFilmes(){
        FilmeDAO dao = new FilmeDAO();
        for(Filme f : dao.read()){
            filmeComboBox.addItem(f);
        }
    }

