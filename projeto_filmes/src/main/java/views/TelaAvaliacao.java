    private void tblAvaliacaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAvaliacaoMouseClicked
    // TODO add your handling code here:
    int linha = tblAvaliacao.getSelectedRow();

    if (linha != -1) {
        // Obtendo o ID da avaliação a partir da tabela
        String id = tblAvaliacao.getValueAt(linha, 0).toString(); 

        AvaliacaoDAO dao = new AvaliacaoDAO();
        minhaAvaliacao = dao.read(Integer.parseInt(id)); 

        // Preenchendo os campos com as informações da avaliação
        txtNota.setText(String.valueOf(minhaAvaliacao.getNota()));
        txtComentario.setText(minhaAvaliacao.getComentario());
        usuarioComboBox.setSelectedItem(minhaAvaliacao.getUsuario());
        filmeComboBox.setSelectedItem(minhaAvaliacao.getFilme());
        }
    }//GEN-LAST:event_tblAvaliacaoMouseClicked


