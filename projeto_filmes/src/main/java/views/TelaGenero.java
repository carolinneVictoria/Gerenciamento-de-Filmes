    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        Genero meuGenero = new Genero();
        meuGenero.setDescricao(txtDescricao.getText());
        
        GeneroDAO dao = new GeneroDAO();
      
        dao.create(meuGenero);
        
        limpar();
       
        preencherTabela();
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void tabelaGenerosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaGenerosMouseClicked
        // TODO add your handling code here:
        int linha = tabelaGeneros.getSelectedRow();
        
        if(linha != -1){
            String id = tabelaGeneros.getValueAt(linha, 0).toString(); 
            GeneroDAO dao = new GeneroDAO();
            //Construindo um objeto de fornecedor a partir do ID
            meuGenero = dao.read(Integer.parseInt(id));
            
            //Preencher o formulario com as informações do objeto
            txtDescricao.setText(meuGenero.getDescricao());
            }
    }//GEN-LAST:event_tabelaGenerosMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int opcao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir?");
        
        if(opcao == JOptionPane.YES_OPTION){
            GeneroDAO dao = new GeneroDAO();
            dao.destroy(meuGenero);
        }
        limpar();
        preencherTabela();
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        meuGenero.setDescricao(txtDescricao.getText());
     
        GeneroDAO dao = new GeneroDAO();
       
        dao.update(meuGenero);
   
        limpar();
        
        //Ação de preencher a tabela de cadastrados (pega o array e td que tiver nele joga na tabela)
        preencherTabela();  
    }//GEN-LAST:event_btnAtualizarActionPerformed


