    public void preencherTabela(){
            DefaultTableModel dtm = (DefaultTableModel) tabelaGeneros.getModel();
            dtm.setRowCount(0);
            GeneroDAO dao = new GeneroDAO();
            
            for(Genero g : dao.read()){
                dtm.addRow(new Object[] {
                    g.getId_genero(),
                    g.getDescricao(),
                });
            }
    }
