    private void tabelaUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaUsuarioMouseClicked
        // TODO add your handling code here:
        int linha = tabelaUsuario.getSelectedRow();
        
        if(linha != -1){
            String id = tabelaUsuario.getValueAt(linha, 0).toString(); // toString = converte p uma string
            UsuarioDAO dao = new UsuarioDAO();
            
            //Construindo um objeto de usuario a partir do ID
            meuUsuario = dao.read(Integer.parseInt(id));
            
            //Preencher o formulario com as informações do objeto
            txtNome.setText(meuUsuario.getNome());
            txtEmail.setText(meuUsuario.getEmail());
            txtSenha.setText(meuUsuario.getSenha());
            
            } 
    }//GEN-LAST:event_tabelaUsuarioMouseClicked


