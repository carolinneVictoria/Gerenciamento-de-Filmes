    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        Usuario meuUsuario = new Usuario(); //Crio um novo objeto da classe
        meuUsuario.setNome(txtNome.getText()); //Pego os dados dos campos de texto e passo para a classe pelo Set
        meuUsuario.setEmail(txtEmail.getText());
        meuUsuario.setSenha(txtSenha.getText());

        //instanciar um objeto da classe DAO
        UsuarioDAO dao = new UsuarioDAO();
        //invocar o metodo create da classe
        dao.create(meuUsuario);

        limpar();
        preencherTabela();
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int opcao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir?");
        
        if(opcao == JOptionPane.YES_OPTION){
            UsuarioDAO dao = new UsuarioDAO();
            dao.destroy(meuUsuario);
        }
        limpar();
        preencherTabela();
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        meuUsuario.setNome(txtNome.getText());
        meuUsuario.setEmail(txtEmail.getText());
        meuUsuario.setSenha(txtSenha.getText());
        
        //instanciar um objeto da classe DAO
        UsuarioDAO dao = new UsuarioDAO();
        
        //invocar o metodo update da classe
        dao.update(meuUsuario);

        limpar();
        preencherTabela();     
    }//GEN-LAST:event_btnAtualizarActionPerformed

