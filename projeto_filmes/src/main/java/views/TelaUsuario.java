    public void preencherTabela(){
            DefaultTableModel dtm = (DefaultTableModel) tabelaUsuario.getModel();
            dtm.setRowCount(0);
            UsuarioDAO dao = new UsuarioDAO();
            
            for(Usuario u : dao.read()){
                dtm.addRow(new Object[] {
                    u.getId_usuario(),
                    u.getNome(),
                    u.getEmail(),
                    u.getData_criacao()
                });
            }
    }
